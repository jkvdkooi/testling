// Lettercombinaties uitgesloten door PostNL
export const postNLuitgesloten = ['SS', 'SA', 'SD', 'SF'];

const alleLetters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'.split('');

export function genereerPC6Letters() {
  let combinatie;
  do {
    const l1 = alleLetters[Math.floor(Math.random() * alleLetters.length)];
    const l2 = alleLetters[Math.floor(Math.random() * alleLetters.length)];
    combinatie = l1 + l2;
  } while (postNLuitgesloten.includes(combinatie));
  return combinatie;
}
